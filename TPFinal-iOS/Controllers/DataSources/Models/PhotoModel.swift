import UIKit

struct PhotoModel: Codable {
    let albumID, id: Int
    let title, url, thumbnailURL: String
    
    enum CodingKeys: String, CodingKey {
        case albumID = "albumId"
        case id, title, url
        case thumbnailURL = "thumbnailUrl"
    }
}
